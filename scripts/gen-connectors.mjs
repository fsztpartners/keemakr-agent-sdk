// Codegen: src/connectors.snapshot.json → src/connectors.generated.ts
//
// The snapshot is a committed copy of keemakr-core's public catalog
// (GET /api/connections/catalog). We inline it into a typed .ts module (rather
// than importing the JSON at runtime) so the build is a plain tsc pass that emits
// dist/connectors.generated.{js,d.ts} with no JSON-module resolution needed under
// NodeNext + verbatimModuleSyntax.
//
// Refresh the manifest after core ships new connectors/operations:
//   curl -s "$KEE_CORE_URL/api/connections/catalog" > src/connectors.snapshot.json
//   npm run gen:connectors
// (or npm run build, which runs gen:connectors first).

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const SNAPSHOT = join(here, '..', 'src', 'connectors.snapshot.json');
const OUT = join(here, '..', 'src', 'connectors.generated.ts');

/** @typedef {{ name: string, description: string|null, input_schema: unknown }} CatalogOp */
/** @typedef {{ provider: string, display_name: string, auth_kind: string, category: string|null, maturity: string, doc_url: string|null, operations: CatalogOp[] }} CatalogProvider */

const raw = readFileSync(SNAPSHOT, 'utf8');
/** @type {{ providers: CatalogProvider[] }} */
const catalog = JSON.parse(raw);
const providers = [...catalog.providers].sort((a, b) => a.provider.localeCompare(b.provider));

// Build the runtime manifest: provider → { maturity, ops: {name → {description, inputSchema}} }.
const manifest = {};
for (const p of providers) {
  const ops = {};
  for (const op of p.operations) {
    ops[op.name] = { description: op.description ?? null, inputSchema: op.input_schema ?? {} };
  }
  manifest[p.provider] = {
    displayName: p.display_name,
    authKind: p.auth_kind,
    category: p.category,
    maturity: p.maturity,
    docUrl: p.doc_url,
    ops,
  };
}

// String-literal unions for provider names + per-provider op names, so callers
// get autocomplete on kee.connections[<provider>].call(<op>).
const providerNames = providers.map((p) => p.provider);
const providerUnion = providerNames.map((n) => JSON.stringify(n)).join(' | ') || 'never';

const opNameByProvider = providers
  .map((p) => {
    const names = p.operations.map((o) => JSON.stringify(o.name));
    const union = names.length ? names.join(' | ') : 'never';
    return `  ${JSON.stringify(p.provider)}: ${union};`;
  })
  .join('\n');

const banner = `// AUTO-GENERATED by scripts/gen-connectors.mjs — DO NOT EDIT BY HAND.
// Source: src/connectors.snapshot.json (a committed copy of keemakr-core's
// GET /api/connections/catalog). Refresh: see scripts/gen-connectors.mjs header.
`;

const body = `${banner}
export type ConnectorName = ${providerUnion};

/** Per-provider operation names (autocomplete for \`.call(op)\`). */
export interface ConnectorOps {
${opNameByProvider}
}

export interface ConnectorOp {
  description: string | null;
  /** JSON Schema (draft 2020-12) for the operation's arguments. */
  inputSchema: unknown;
}

export interface ConnectorInfo {
  displayName: string;
  authKind: string;
  category: string | null;
  /** 'ready' → connectable + operations callable; 'coming_soon' → declared, ops not yet wired. */
  maturity: string;
  docUrl: string | null;
  ops: Record<string, ConnectorOp>;
}

/**
 * The connector catalog as of the last snapshot refresh. Metadata only — no
 * credentials. Keyed by provider slug; each entry lists its callable operations
 * and their JSON-Schema arg contracts.
 */
export const connectors = ${JSON.stringify(manifest, null, 2)} as const satisfies Record<ConnectorName, ConnectorInfo>;
`;

writeFileSync(OUT, body);
const opCount = providers.reduce((n, p) => n + p.operations.length, 0);
console.log(
  `[gen-connectors] wrote ${OUT} — ${providers.length} providers, ${opCount} operations`,
);
